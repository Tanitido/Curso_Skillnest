# https://docs.python.org/3/library/math.html
import math  # importamos la libreria math que trae funciones adicionales

# a continuacion las funciones basicas de python
print(round(1.3))  # 1 redondea al entero mas cercano
print(round(1.6))  # 2 redondea al entero mas cercano
print(round(1.5))  # 2 redondea al entero mas cercano
print(abs(-1))  # 1 valor absoluto
print(abs(1))  # 1 valor absoluto

# funciones de la libreria math
print(math.ceil(1.3))  # 2 redondea al entero superior
print(math.floor(1.99999))  # 1 redondea al entero inferior
print(math.isnan(23))  # False no es un numero
# print(math.isnan("23"))  este tira error porque no es un numero
print(math.pow(2, 3))  # 8 eleva el primer numero al segundo
print(math.sqrt(9))  # 3 raiz cuadrada
